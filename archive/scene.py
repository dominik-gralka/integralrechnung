from manim import *


class SquareToCircle(Scene):
    def construct(self):
        circle = Circle()  # create a circle
        circle.set_fill(PINK, opacity=0.5)  # set color and transparency

        square = Square()  # create a square
        square.rotate(PI / 4)  # rotate a certain amount

        triangle = Triangle()
        triangle.next_to(circle)
        triangle.set_fill(BLUE, opacity=0.5)


        self.play(FadeIn(square))
        self.wait(1)
        self.play(Transform(square, circle))  # interpolate the square into the circle
        self.wait(3)
        self.play(Transform(circle, triangle))
        self.play(FadeOut(square))  # fade out animation
        self.play(circle.animate.shift(LEFT * 2))
        self.play(Rotate(circle, 0.5))
        self.wait(1)
        self.play(circle.animate.scale(1.5))
        self.play(circle.animate.set_fill(BLUE, opacity=1))

